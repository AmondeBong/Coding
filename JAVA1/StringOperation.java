
public class StringOperation{
    public static void main(String[] args){
        System.out.println("Hello World".length()); // 글자수 알려줌 " ".length()
        System.out.println("Hello, leeche ...bye .".replace("leeche","egoing")); //replace("원래 글자","바꿀 글자") 대체
        System.out.println("Hello, [[[name]]] ...bye .".replace("[[[name]]]","leeche"));
    }
}